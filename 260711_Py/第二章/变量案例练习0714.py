#交换 a,b两个变量的值,并输出到控制台
a, b = 10, "hello"
print("交换前: a->{}, b->{}".format (a , b))
c = a
a = b
b = c
print(f"交换后: a->{a}, b->{b}")

#现在有三个变量a=100,b=200,c=300,现将三个变量值进行交换,将a,b,c的值分别赋值给c,a,b,并输出到控制台
a, b, c = 100, 200, 300
print(f"交换前: a->{a}, b->{b}, c->{c}")
#可以使用元组解包的方式来赋值(a->c,b->a,c->b)
#赋值时会先把等号右边(b, c, a)的值打包成一个临时元组（即(200, 300, 100)），然后再按顺序依次解包赋值给左边的 a, b, c
c, a, b = a, b, c
# d = c
# c = a
# a = b
# b = d
print(f"交换后: a->{a}, b->{b}, c->{c}")

