# -------- 练习 1：个人信息卡片输出程序 --------
#
# 涉及知识点：
#   - 数字字面量（int / float）
#   - 字符串字面量（str）
#   - 布尔字面量（bool）
#   - None 字面量
#   - 变量赋值
#   - type() 查看类型
#   - f-string 格式化输出
#
# 题目描述：
#   用变量存储个人信息（姓名、年龄、身高、是否学生、爱好等），
#   使用 f-string 格式化输出一张自我介绍名片。
#   然后使用 type() 打印每个变量的数据类型。
#
# 要求：
#   1. 至少包含 5 个不同类型的变量（str / int / float / bool / NoneType）
#   2. 输出格式整齐，可以使用边框装饰
#   3. 最后输出每个字段的类型信息
#
# 示例输出结构：
#   +----------------------------------------+
#   |  个人名片
#   |  姓名：张三
#   |  年龄：25 岁
#   |  身高：175.5 cm
#   |  是否学生：False
#   |  爱好：None（待定）
#   +----------------------------------------+
#   各字段类型：
#     my_name 类型：<class 'str'>
#     my_age 类型：<class 'int'>
#     ...
my_name = "mo_sifu"
my_age = 23
my_height = 168
my_is_student = False
my_hobby = None
print("+-" * 20)
print(f"|  个人名片")
print(f"|  姓名：{my_name}")
print(f"|  年龄：{my_age} 岁")
print(f"|  身高：{my_height} cm")
print(f"|  是否学生：{my_is_student}")
print(f"|  爱好：{my_hobby}")
print("+-" * 20)
print(f"各字段类型：")
print(f"    my_name 类型：{type(my_name)}")
print(f"    my_age 类型：{type(my_age)}")
print(f"    my_height 类型：{type(my_height)}")
print(f"    my_is_student 类型：{type(my_is_student)}")
print(f"    my_hobby 类型：{type(my_hobby)}")
